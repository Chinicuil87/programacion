package programacion.java.CodigoFacilito;

public class DeclaacionVariables {

    public static void main(String[] args) {

        
        // 4 tipos de datos
        // Enteros
        // Decimales
        // Caracteres
        // Bolleanos

        /*
         * Se recomida nombre descriptivos
         * Evitar palabras reservadas (class, float, interget)
         * Usar formato CamelCase para en nombre de variables.
         */

        int version; // Se declara una variable de tipo entero
        version = 9; // Se le asigna un valor a la variable.
        System.out.println(version);  // Se muestra el valor de version en pantalla.
        version = 20; // Se puede reasignar el valir a una varable
        System.out.println(version); 
        
    }

}
